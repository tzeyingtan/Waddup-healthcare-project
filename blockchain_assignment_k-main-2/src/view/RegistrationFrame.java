    private String generateID(String role, String filePath) {

        // Read the last ID from the file
        String lastID = getLastID(role, filePath);

        // Generate the new ID based on the last ID
        String newID;
        if (lastID == null || lastID.isEmpty()) {
            newID = role.substring(0, 1) + "1";
        } else {
            int num = Integer.parseInt(lastID.substring(1)) + 1;
            newID = role.substring(0, 1) + num;
        }

        return newID;
    }

    private String getLastID(String role, String filePath) {
        File file = new File(filePath);
        if (!file.exists()) {
            return null;
        }

        try (Scanner scanner = new Scanner(file)) {
            String lastID = null;
            while (scanner.hasNextLine()) {
                String line = scanner.nextLine();
                String[] parts = line.split(",");
                if (parts.length > 0) {
                    lastID = parts[0];
                }
            }
            return lastID;
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error reading file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }
