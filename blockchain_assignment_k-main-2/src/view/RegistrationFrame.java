    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

        try {
            String name = txtCName.getText();
            String contact = txtCContact.getText();
            String registrationCode = txtCCode.getText(); // Assuming this is the company registration code
            String role = (String) comRegisterAs.getSelectedItem();
            String password = txtCPass.getText(); // Get the password input

            User u = null;
            
            if (role.equals("Manufacturer")) {
                u = manufacturerController.createNewUser(name, registrationCode, password, contact);
            } else if (role.equals("HealthcareProvider")) {
                u = healthcareProviderController.createNewUser(name, registrationCode, password, contact);
            } else if (role.equals("Distributor")) {
                u = distributorController.createNewUser(name, registrationCode, password, contact);
            }

            // Generate keypair
            MyKeyPair.create();
            // Store private key
            MyKeyPair.put(MyKeyPair.getPrivateKey().getEncoded(), u.getPrivateKeyPath());
            // Store public key
            MyKeyPair.put(MyKeyPair.getPublicKey().getEncoded(), u.getPublicKeyPath());

            JOptionPane.showMessageDialog(this, "Registration successful! Your ID is: " + u.getID() + ". Use this ID for login.");
        } catch (Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error writing to file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

